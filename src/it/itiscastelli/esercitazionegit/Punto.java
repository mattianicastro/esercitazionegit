package it.itiscastelli.esercitazionegit;

public class Punto {
    private final double x;
    private final double y;

    public Punto(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
